package objectLists;

public class OfferedList {
	private String course;//ClassList - classCode  (subject acronym with course number)
	private String termOffered;//What semester is this course offered.
	
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getTermOffered() {
		return termOffered;
	}
	public void setTermOffered(String termOffered) {
		this.termOffered = termOffered;
	}
	
	
}
